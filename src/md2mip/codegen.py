"""IR → standalone Python solver script."""

from __future__ import annotations

import ast
import re
from md2mip.ir import IR, Constraint, Variable


def generate(ir: IR) -> str:
    """Generate a standalone Python solver script from an IR."""
    ir.validate()
    parts = [
        _header(ir),
        _load_data(ir),
        _build_model(ir),
        *_constraint_functions(ir),
        _solve_and_report(ir),
        _main(),
    ]
    return "\n\n".join(parts) + "\n"


# ---------------------------------------------------------------------------
# Variable layout helpers
# ---------------------------------------------------------------------------

def _var_layout(ir: IR) -> list[tuple[str, Variable, str]]:
    """Return (name, var, offset_expr) for each variable group in flat index order."""
    layout = []
    for name, var in ir.variables.items():
        layout.append((name, var, _offset_expr(name, ir)))
    return layout


def _var_size_expr(var: Variable, ir: IR) -> str:
    """Return a Python expression for the number of elements in a variable group."""
    if not var.indices:
        return "1"
    parts = [f"n_{s.lower()}" for s in var.indices]
    return " * ".join(parts)


def _total_vars_expr(ir: IR) -> str:
    parts = []
    for name, var in ir.variables.items():
        parts.append(_var_size_expr(var, ir))
    return " + ".join(parts)


# ---------------------------------------------------------------------------
# Code sections
# ---------------------------------------------------------------------------

def _header(ir: IR) -> str:
    return f'''#!/usr/bin/env python3
"""{ir.name} model — generated by md2mip"""

import argparse
import json
import sys

import highspy
import numpy as np
import yaml'''


def _load_data(ir: IR) -> str:
    lines = [
        'def load_data(path: str) -> dict:',
        '    """Load data from YAML or JSON into numpy arrays."""',
        '    with open(path) as f:',
        '        if path.endswith(".json"):',
        '            raw = json.load(f)',
        '        else:',
        '            raw = yaml.safe_load(f)',
        '    data = {}',
    ]
    for sname in ir.sets:
        lines.append(f'    data["{sname}"] = list(raw["{sname}"])')
    for pname, param in ir.parameters.items():
        if param.indices:
            lines.append(f'    data["{pname}"] = np.array(raw["{pname}"], dtype=np.float64)')
        else:
            if param.default is not None:
                lines.append(f'    data["{pname}"] = float(raw.get("{pname}", {param.default}))')
            else:
                lines.append(f'    data["{pname}"] = float(raw["{pname}"])')
    lines.append('    return data')
    return "\n".join(lines)


def _build_model(ir: IR) -> str:
    lines = ['def build_model(data: dict) -> highspy.Highs:']

    # Set sizes
    for sname in ir.sets:
        lines.append(f'    n_{sname.lower()} = len(data["{sname}"])')

    lines.append('')
    lines.append('    h = highspy.Highs()')
    lines.append('    h.silent()')
    lines.append('')

    # Total variables
    n_vars_expr = _total_vars_expr(ir)
    lines.append(f'    n_vars = {n_vars_expr}')
    lines.append('')

    # Add variables
    layout = _var_layout(ir)
    for vname, var, _offset in layout:
        size_expr = _var_size_expr(var, ir)
        lb = var.lower_bound if var.lower_bound is not None else 0
        ub_val = var.upper_bound
        if var.type == "binary":
            lb, ub_val = 0, 1
        lb_str = f"np.zeros({size_expr})" if lb == 0 else f"np.full({size_expr}, {lb})"
        if ub_val is None:
            ub_str = f"np.full({size_expr}, np.inf)"
        else:
            ub_str = f"np.full({size_expr}, {ub_val})"
        lines.append(f'    # --- Variable: {vname}{_index_comment(var)} ---')
        lines.append(f'    h.addVars({size_expr}, {lb_str}, {ub_str})')
        if var.type == "binary":
            offset_expr = _offset_expr(vname, ir)
            lines.append(f'    for _k in range({size_expr}):')
            lines.append(f'        h.changeColIntegrality({offset_expr} + _k, highspy.HighsVarType.kInteger)')

    lines.append('')

    # Objective
    lines.append('    # --- Objective ---')
    obj = ir.objective
    obj_code = _generate_objective(obj.expression, ir)
    lines.extend(f'    {l}' for l in obj_code)
    sense = "kMinimize" if obj.sense == "minimize" else "kMaximize"
    lines.append(f'    h.changeObjectiveSense(highspy.ObjSense.{sense})')
    lines.append('')

    # Constraint groups
    for cname in ir.constraints:
        fn_name = f"setup_{cname}_constraints"
        args = _constraint_fn_args(ir)
        lines.append(f'    {fn_name}({args})')

    lines.append('')
    lines.append('    return h')
    return "\n".join(lines)


def _index_comment(var: Variable) -> str:
    if not var.indices:
        return ""
    idx = ",".join(s.lower() for s in var.indices)
    return f"[{idx}]"


def _offset_expr(vname: str, ir: IR) -> str:
    """Return a Python expression for the flat offset of variable group vname."""
    offset_parts = []
    for name, var in ir.variables.items():
        if name == vname:
            break
        offset_parts.append(_var_size_expr(var, ir))
    if not offset_parts:
        return "0"
    return " + ".join(offset_parts)


def _constraint_fn_args(ir: IR) -> str:
    parts = ["h", "data"]
    for sname in ir.sets:
        parts.append(f"n_{sname.lower()}")
    return ", ".join(parts)


def _parse_comparison(expr_str: str) -> tuple[str, str, str]:
    """Split 'lhs <= rhs' into (lhs, op, rhs). Also handles >=, ==."""
    for op in ("<=", ">=", "=="):
        if op in expr_str:
            parts = expr_str.split(op, 1)
            return parts[0].strip(), op, parts[1].strip()
    raise ValueError(f"No comparison operator found in: {expr_str}")


