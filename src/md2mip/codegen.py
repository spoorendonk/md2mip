# Objective code generation
# ---------------------------------------------------------------------------

def _generate_objective(expr: str, ir: IR) -> list[str]:
    """Generate code to set objective coefficients."""
    lines = []
    lines.append(f"obj_coeffs = np.zeros(n_vars)")

    # Parse the objective expression to find sum(coeff * var for ...)
    # We handle: sum(p[i]*x[i] ...) and sum(f[i]*y[i] ...) + sum(c[i,j]*x[i,j] ...)
    terms = _split_top_level_sums(expr)
    for term in terms:
        lines.extend(_gen_obj_term(term, ir))

    lines.append("h.changeColsCost(n_vars, np.arange(n_vars, dtype=np.int32), obj_coeffs)")
    return lines


def _split_top_level_sums(expr: str) -> list[str]:
    """Split 'sum(...) + sum(...)' into individual sum expressions."""
    # Find top-level + between sum() calls
    results = []
    depth = 0
    current = []
    i = 0
    while i < len(expr):
        ch = expr[i]
        if ch == '(':
            depth += 1
            current.append(ch)
        elif ch == ')':
            depth -= 1
            current.append(ch)
        elif ch == '+' and depth == 0:
            results.append("".join(current).strip())
            current = []
        else:
            current.append(ch)
        i += 1
    if current:
        results.append("".join(current).strip())
    return [r for r in results if r]


def _gen_obj_term(term: str, ir: IR) -> list[str]:
    """Generate code for one sum(...) term in the objective."""
    lines = []
    # Parse: sum(body for idx1 in Set1 for idx2 in Set2)
    m = re.match(r'sum\((.+)\)', term, re.DOTALL)
    if not m:
        # Scalar term like "K * z[t]" — shouldn't normally appear at top level
        return [f"# TODO: non-sum objective term: {term}"]

    inner = m.group(1).strip()

    # Split off 'for ...' clauses
    body, iterators = _split_body_and_iterators(inner)

    # Parse iterators: "i in I", "j in J"
    idx_vars = []
    idx_sets = []
    for it in iterators:
        parts = it.strip().split(" in ")
        idx_vars.append(parts[0].strip())
        idx_sets.append(parts[1].strip())

    # The body might have multiple additive terms: p * x[t] + h * Inv[t] + K * z[t]
    # Split into individual terms and handle each
    body_terms = _split_additive_terms(body)

    # Generate nested loop
    indent = ""
    for iv, iset in zip(idx_vars, idx_sets):
        size = f"n_{iset.lower()}"
        lines.append(f"{indent}for {iv} in range({size}):")
        indent += "    "

    for sign, bterm in body_terms:
        bterm = bterm.strip()
        coeff_expr, var_name, var_indices = _parse_product(bterm, ir)
        if var_name not in ir.variables:
            lines.append(f"{indent}# TODO: non-variable term: {bterm}")
            continue

        offset = _offset_expr(var_name, ir)
        flat_idx = _flat_index_expr(var_name, var_indices, ir)
        effective_coeff = coeff_expr if sign == "+" else _negate(coeff_expr)
        lines.append(f"{indent}obj_coeffs[{offset} + {flat_idx}] += {_coeff_code(effective_coeff, ir)}")

    return lines


def _split_body_and_iterators(inner: str) -> tuple[str, list[str]]:
    """Split 'coeff * var for i in I for j in J' into body and iterator list."""
    # Find first ' for ' that's not inside brackets
    parts = re.split(r'\s+for\s+', inner)
    body = parts[0]
    iterators = parts[1:]
    return body, iterators


def _parse_product(body: str, ir: IR) -> tuple[str, str, list[str]]:
    """Parse 'c[i,j] * x[i,j]' into (coeff_expr, var_name, var_indices).

    Also handles: 'x[i,j]' (coeff=1), 'p * x[t]' (scalar param * var).
    """
    body = body.strip()

    # Try to parse as AST
    tree = ast.parse(body, mode='eval')
    node = tree.body

    return _extract_product(node, ir)


def _extract_product(node: ast.expr, ir: IR) -> tuple[str, str, list[str]]:
    """Extract (coeff_source, var_name, var_indices) from an AST node."""
    if isinstance(node, ast.BinOp) and isinstance(node.op, ast.Mult):
        # A * B — figure out which side is the variable
        left_var = _find_var_ref(node.left, ir)
        right_var = _find_var_ref(node.right, ir)

        if right_var:
            vname, vindices = right_var
            coeff = ast.unparse(node.left)
            return coeff, vname, vindices
        elif left_var:
            vname, vindices = left_var
            coeff = ast.unparse(node.right)
            return coeff, vname, vindices
    elif isinstance(node, ast.Subscript):
        vref = _find_var_ref(node, ir)
        if vref:
            return "1", vref[0], vref[1]

    # Fallback
    return ast.unparse(node), "_unknown", []


def _find_var_ref(node: ast.expr, ir: IR) -> tuple[str, list[str]] | None:
    """If node is var[i,j], return (var_name, [i, j])."""
    if isinstance(node, ast.Subscript) and isinstance(node.value, ast.Name):
        name = node.value.id
        if name in ir.variables:
            indices = _extract_subscript_indices(node.slice)
            return name, indices
    return None


def _extract_subscript_indices(node: ast.expr) -> list[str]:
    """Extract index names from a subscript: x[i,j] -> ['i', 'j'], x[i] -> ['i']."""
    if isinstance(node, ast.Tuple):
        return [ast.unparse(e) for e in node.elts]
    return [ast.unparse(node)]


def _flat_index_expr(var_name: str, indices: list[str], ir: IR) -> str:
    """Compute flat index expression for var[i,j,...] given the variable's index sets."""
    var = ir.variables[var_name]
    if len(indices) == 0:
        return "0"
    if len(indices) == 1:
        return indices[0]
    # Row-major: i * n_j + j for indices [i, j] with sets [I, J]
    # General: idx[0] * prod(sizes[1:]) + idx[1] * prod(sizes[2:]) + ... + idx[-1]
    sizes = [f"n_{s.lower()}" for s in var.indices]
    parts = []
    for k, idx in enumerate(indices):
        if k < len(indices) - 1:
            suffix = " * ".join(sizes[k + 1:])
            parts.append(f"{idx} * {suffix}")
        else:
            parts.append(idx)
    return " + ".join(parts)


def _coeff_code(coeff_expr: str, ir: IR) -> str:
    """Convert a coefficient expression to Python code referencing data dict."""
    if coeff_expr == "1":
        return "1.0"
    # Replace param references: c[i,j] -> data["c"][i,j], p -> data["p"]
    tree = ast.parse(coeff_expr, mode='eval')
    return _coeff_node_to_code(tree.body, ir)


def _coeff_node_to_code(node: ast.expr, ir: IR) -> str:
    """Convert an AST node for a coefficient to Python code."""
    if isinstance(node, ast.Subscript) and isinstance(node.value, ast.Name):
        name = node.value.id
        if name in ir.parameters:
            indices = _extract_subscript_indices(node.slice)
            idx_str = ", ".join(indices)
            return f'data["{name}"][{idx_str}]'
    if isinstance(node, ast.Name):
        name = node.id
        if name in ir.parameters:
            return f'data["{name}"]'
    if isinstance(node, ast.BinOp):
        left = _coeff_node_to_code(node.left, ir)
        right = _coeff_node_to_code(node.right, ir)
        op_map = {ast.Add: '+', ast.Sub: '-', ast.Mult: '*', ast.Div: '/'}
        op_str = op_map.get(type(node.op), '+')
        return f"({left} {op_str} {right})"
    if isinstance(node, ast.UnaryOp) and isinstance(node.op, ast.USub):
        inner = _coeff_node_to_code(node.operand, ir)
        return f"-({inner})"
    if isinstance(node, ast.Constant):
        return repr(node.value)
    # Fallback
    return ast.unparse(node)


